<h3><strong>Unaccompanied Minors</strong></h3>
Who is an Unaccompanied Minor/Young Passenger and the conditions for travelling?

Below is a straight forward explanation:
<ul>
    <li>Unaccompanied Minors : Any passengers below the age of 12 years.</li>
    <li>Young Passengers : Passengers between the ages of 12-16 years.</li>
</ul>
Many airlines provide (or require) supervision service for minors from the time of boarding until the time the minor is met at the destination. This is called <strong>unaccompanied minor service</strong>. The service provided for the minors, must first be approved by the Airline.

However, airline policies regarding unaccompanied minors vary.

For example, depending on the airline, unaccompanied minor service is often mandatory for children ages 5-14, but optional for teenagers (ages 15-17). Flycorpo recommends the customers to check with the airline for specific policies, fees and services that the airline may cost.

Younger children (typically under the age of 5) are often not eligible for unaccompanied minor service and must be accompanied on the same flight and in the same compartment by an older passenger.

However, passengers between the ages of 12 and 17, who are charged the same as adults are not eligible to accompany Unaccompanied Minors in the absence of a passenger who is 18 years or older.

A fee is usually charged for unaccompanied minor service.

With unaccompanied minor service, the airline will usually want to know these couple of things:
<ul>
    <li>Who is delivering the minor at the airport of departure?</li>
    <li>Who is meeting the minor at the destination?</li>
</ul>
Proof of identification of the receiving adult is often required and Forgetting this crucial identification can cause unpleasant delays or disruptions. Hence, the best solution is to be aware of the airline's expectations and protocols.

Also, please note that minors of certain nationalities travelling outside their country unaccompanied or with someone other than their parent or legal guardian (as identified in their passport), are subject to special requirements. In many cases, the minors are required to carry a letter of travel consent, duly signed by legal guardian or parent with legal custody with authorization to travel. This letter should be carried by the individual at all time.

Booking for unaccompanied minors is only available via phone.

Again, it is recommended to be informed about the airline prior policies before purchasing tickets and inquire about unaccompanied minors.



